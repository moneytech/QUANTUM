// =============================================================================
// Types
// -----------------------------------------------------------------------------
// Semantic boolean type of machine word size
typedef uint bint;

// =============================================================================
// Macros
// -----------------------------------------------------------------------------
// Convert to boolean value
#define clamp(val) (!!(val))

// -----------------------------------------------------------------------------
// Safe assignment to `bool` variables (for Microsoft C compiler)
#if HAVE(C99) || (C_MSC_VER >= 1800)
  #define as_bool(val) (val)
#else
  #define as_bool(val) clamp (val)
#endif

